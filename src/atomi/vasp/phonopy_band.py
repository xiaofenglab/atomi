def write_standalone_script(path: Path) -> None:
    path.write_text(standalone_script_text(), encoding="utf-8")
    path.chmod(0o755)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Plot phonopy band.yaml to a PNG.")
    parser.add_argument("--band-yaml", type=Path, default=Path("band.yaml"))
    parser.add_argument("--outpng", type=Path, default=Path("phonon_band.png"))
    parser.add_argument("--title", default="Phonon band structure")
    parser.add_argument("--ylabel", default="Frequency (THz)")
    return parser


def main(argv: list[str] | None = None) -> None:
    args = build_parser().parse_args(argv)
    plot_band_structure(args.band_yaml, args.outpng, args.title, args.ylabel)
    print(f"Saved: {args.outpng}")


if __name__ == "__main__":
    main()
