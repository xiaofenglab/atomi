def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="atomi pdf-fit-bridge",
        description="Bridge Atomi to DiffPy/PDFgui-style and RMCProfile-style PDF fitting workflows.",
    )
    subparsers = parser.add_subparsers(dest="command", required=True)

    for name in ("status", "doctor"):
        status_parser = subparsers.add_parser(name, help="Check DiffPy/PDFgui/RMCProfile backend availability.")
        status_parser.add_argument("--python", default=None, help="Python interpreter to inspect, e.g. ~/m_lammps_env/bin/python.")
        status_parser.add_argument("--json", action="store_true")
        status_parser.add_argument("--write", default=None, help="Optional JSON output path.")
        status_parser.set_defaults(func=print_status)

    definitions = subparsers.add_parser("definitions", help="Print PDF/RDF function definitions and guardrails.")
    definitions.add_argument("--json", action="store_true")
    definitions.add_argument("--write", default=None, help="Optional JSON output path.")
    definitions.set_defaults(func=print_definitions)

    diffpy = subparsers.add_parser("write-diffpy-script", help="Create a DiffPy small-box G(r) fitting template.")
    diffpy.add_argument("--structure", required=True, help="CIF/POSCAR/structure file accepted by diffpy.structure.")
    diffpy.add_argument("--exp", required=True, help="Experimental two-column r, G(r) file.")
    diffpy.add_argument("--outdir", default="pdf_fit_diffpy_project")
    diffpy.add_argument("--script-name", default="run_diffpy_pdf_fit.py")
    diffpy.add_argument("--rmin", type=float, default=1.5)
    diffpy.add_argument("--rmax", type=float, default=20.0)
    diffpy.add_argument("--qmax", type=float, default=25.0)
    diffpy.add_argument("--qdamp", type=float, default=0.04)
    diffpy.add_argument("--qbroad", type=float, default=0.02)
    diffpy.add_argument("--delta2", type=float, default=2.0)
    diffpy.set_defaults(func=write_diffpy_script)

    rmc = subparsers.add_parser("write-rmcprofile-project", help="Create a guarded RMCProfile large-box project skeleton.")
    rmc.add_argument("--outdir", default="pdf_fit_rmcprofile_project")
    rmc.add_argument("--structure", default=None)
    rmc.add_argument("--real-space-data", default=None)
    rmc.add_argument("--sq-data", default=None)
    rmc.add_argument("--bragg-profile", default=None)
    rmc.add_argument("--real-space-function", default="T(r)", choices=("T(r)", "G(r)", "D(r)", "g(r)"))
    rmc.set_defaults(func=write_rmcprofile_project)

    install_plan = subparsers.add_parser("install-plan", help="Print installation guidance for the fitting backends.")
    install_plan.add_argument("--env", default="~/m_lammps_env")
    install_plan.add_argument("--json", action="store_true")
    install_plan.set_defaults(func=print_install_plan)

    return parser


def main(argv: list[str] | None = None) -> None:
    args = build_parser().parse_args(argv)
    args.func(args)
